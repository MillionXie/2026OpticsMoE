import json
import importlib.util
import sys
from pathlib import Path
from types import SimpleNamespace

VIS_DIR = Path(__file__).resolve().parent
if str(VIS_DIR) not in sys.path:
    sys.path.insert(0, str(VIS_DIR))
_COMMON_SPEC = importlib.util.spec_from_file_location("visualization_local_common", VIS_DIR / "common.py")
_COMMON = importlib.util.module_from_spec(_COMMON_SPEC)
_COMMON_SPEC.loader.exec_module(_COMMON)

import plot_final_comparison
import plot_generalization_gap
import plot_prompt_history
import plot_expert_usage
import plot_time_comparison
import plot_training_curves

add_common_args = _COMMON.add_common_args
ensure_out_dir = _COMMON.ensure_out_dir


def _clone_args(args, **updates):
    payload = vars(args).copy()
    payload.update(updates)
    return SimpleNamespace(**payload)


def make_report(args):
    out_dir = ensure_out_dir(args.out_dir)
    outputs = {}
    try:
        outputs["training_curves"] = plot_training_curves.make_plot(
            _clone_args(args, metrics=["acc", "loss"], metric=None, show=["train", "val"], mode="overlay", smooth=0, show_best=False, show_phase_dropout=True)
        )
    except SystemExit as exc:
        outputs["training_curves_warning"] = str(exc)
    try:
        outputs["final_acc"] = plot_final_comparison.make_plot(
            _clone_args(args, metric="final_test_acc", x="model_type", hue=None, title="Final Test Accuracy", name=(args.name or "report") + "_final_acc")
        )
    except SystemExit as exc:
        outputs["final_acc_warning"] = str(exc)
    try:
        outputs["time"] = plot_time_comparison.make_plot(
            _clone_args(args, metric="total_train_time_sec", unit="min", name=(args.name or "report") + "_training_time")
        )
    except SystemExit as exc:
        outputs["time_warning"] = str(exc)
    try:
        outputs["generalization_gap"] = plot_generalization_gap.make_plot(_clone_args(args, name=(args.name or "report") + "_generalization_gap"))
    except SystemExit as exc:
        outputs["generalization_gap_warning"] = str(exc)
    try:
        outputs["expert_usage"] = plot_expert_usage.make_plot(
            _clone_args(args, value="normalized_prompt_power", epoch="final", name=(args.name or "report") + "_expert_usage")
        )
    except SystemExit as exc:
        outputs["expert_usage_warning"] = str(exc)
    try:
        outputs["prompt_history"] = plot_prompt_history.make_plot(
            _clone_args(args, value="normalized_prompt_power", top_k=0, name=(args.name or "report") + "_prompt_power_history")
        )
    except SystemExit as exc:
        outputs["prompt_history_warning"] = str(exc)
    with open(out_dir / "report_summary.json", "w", encoding="utf-8") as handle:
        json.dump(outputs, handle, indent=2, ensure_ascii=False)
    with open(out_dir / "README.md", "w", encoding="utf-8") as handle:
        handle.write("# Single-Task Plot Report\n\n")
        handle.write("This folder was generated by `make_single_task_report.py`.\n\n")
        for key, value in outputs.items():
            handle.write(f"- `{key}`: {value}\n")
    return outputs


def main():
    import argparse

    parser = argparse.ArgumentParser(description="Generate a compact figure report for selected single-task runs.")
    add_common_args(parser)
    args = parser.parse_args()
    make_report(args)


if __name__ == "__main__":
    main()
